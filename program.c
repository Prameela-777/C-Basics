/* C program to print age, cgpa and initial of the student */
#include<stdio.h>
void main()
{
	// variable Declaration
	int age;
	float cgpa;
	char initial;
	
	//Initialization
	age = 21;
	cgpa = 8.4;
	initial = 'R'; // you have to enclose a character using single quotes
	
	// output
	printf("Age of the student is %d\n", age);
	printf("CGPA of the student is %f\n", cgpa);
	printf("Student surname starts with %c", initial);  
}

	


